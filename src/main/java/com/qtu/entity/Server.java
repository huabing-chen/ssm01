package com.qtu.entity;

public class Server {
    private Integer id;

    private Integer serno;

    private String sername;

    private Double serprice;

    private Integer sernum;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSerno() {
        return serno;
    }

    public void setSerno(Integer serno) {
        this.serno = serno;
    }

    public String getSername() {
        return sername;
    }

    public void setSername(String sername) {
        this.sername = sername;
    }

    public Double getSerprice() {
        return serprice;
    }

    public void setSerprice(Double serprice) {
        this.serprice = serprice;
    }

    public Integer getSernum() {
        return sernum;
    }

    public void setSernum(Integer sernum) {
        this.sernum = sernum;
    }
}